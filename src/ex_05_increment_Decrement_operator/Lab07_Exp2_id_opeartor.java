package ex_05_increment_Decrement_operator;

public class Lab07_Exp2_id_opeartor {
    public static void main(String[] args) {
        int a = 10;
        System.out.println(++a + ++a);
        System.out.println(a);

        // Exp A => ++a => 11 ; a= 11
        // Exp B => ++a => 12 ; a= 12
        // Exp A + Exp B => 11 + 12 = 23
        // 5 | 10 | NA
        // 6 | 12 | 23


    }
}
