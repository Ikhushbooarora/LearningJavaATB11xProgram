package ex_05_increment_Decrement_operator;

public class Lab08_decrement_operator {
    public static void main(String[] args) {
        int a = 10;
        int result_post = a--;
        System.out.println(result_post);
        System.out.println(a);

        // 5 | 10 | na
        // 6 | na | 10
        // 7 | na | 10
        // 8 | 9 | na

    }
}
