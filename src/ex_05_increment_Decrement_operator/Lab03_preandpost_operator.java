package ex_05_increment_Decrement_operator;

public class Lab03_preandpost_operator {
    public static void main(String[] args) {
        int a = 5;
        System.out.println(++a);
        System.out.println(a);


        // post increment = print first and then increase
        int b = 10;
        System.out.println(b++);
        System.out.println(b);




    }
}
