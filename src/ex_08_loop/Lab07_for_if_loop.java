package ex_08_loop;

public class Lab07_for_if_loop {
    public static void main(String[] args) {
        for (int khushboo = 0; khushboo < 18; khushboo++) {
            if (khushboo > 15) {
                System.out.println("gift from papa");
            } else {
                System.out.println("no gift");
            }
        }
    }
}
