package ex_04_ternary_operator;

public class Lab01_ternary_operator {
    public static void main(String[] args) {
        // ? : -> ternary operator condition
       int age = 15;
       String canivote = age>=18?"yes you can vote" : "no, you cant vote";
       System.out.println(canivote);


        int age1 = 20;
        String canivote1 = age1>=18?"yes you can vote" : "no, you cant vote";
        System.out.println(canivote1);



    }
}
